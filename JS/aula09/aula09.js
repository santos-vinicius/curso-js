// Operadores Aritméticos
const num1 = 5;
const num2 = 10;
const dez = '10';

// Adição e Concatenação
console.log(num1 + num2);
console.log(num1 + dez);

// Subtração
console.log(num1 - num2);

// Multiplicação
console.log(num1 * num2);

// Divisão
console.log(num1 / num2);

// Potenciação
console.log(num1 ** num2);

// Resto da divisão
console.log(num2 % num1);

